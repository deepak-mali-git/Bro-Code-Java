public class Variables {
    public static void main(String[] args) {
        

        // variable = a placeholder for the value that behaves as the value it contains

    //DATA TYPE     VARIABLE    SIZE            PRIMITIVE/REFERENCE         VALUE
        boolean        bo;     // 1 bit             primitive           True or False
        byte           by;     // 1 byte            primitive           -128 to 127
        short          sh;     // 2 byte            primitive           -32,768 to 32,767
        int            in;     // 4 byte            primitive           -2 billion to 2 b
        long           lo;     // 8 byte            primitive           -9 quintillion to 9 q

        float          fl;     // 4 byte            primitive           factorial number 6-7 digit
        double         db;     // 8 byte            primitive           up to 15 digit

        char           ch;     // 2 byte            primitive           single character 
        String         st;     // varies            Reference           sequence of character


        int x; // declaration
        x = 10; // assignment
        
        long y = 12345L; // initialization
        float f = 12.34f;
        

        System.out.println("My number is " + x);
    }
}